console.log("Primitive Data Types")
//Number
var a5 = 10;
let b5 = 20;
const c5 = 30;
console.log(a5);
console.log(b5);
console.log(c5);

//float

var a6 = 10.45;
let b6 = 34.56;
const c6 = 35.78;
console.log(a6);
console.log(b6);
console.log(c6);

//String

var a7 = "Sai";
let b7 = "10";
const c7 = "45.67";
console.log(a7);
console.log(b7);
console.log(c7);

//Boolean
var a8 = true;
let b8 = false;
let c8 = true;
console.log(a8);
console.log(b8);
console.log(c8);

//Undefined

var a9;
let b9;
console.log(a9);
console.log(b9);

//Null

let a4 = null;
var b4 = null;
const c4 = null;
console.log(a4);
console.log(b4);
console.log(c4);

//Symbol

let a3 = Symbol("sai");
var b3 = Symbol(123);
const c3 = Symbol(34.56);
console.log(a3);
console.log(b3);
console.log(c3);

//Special Data types
//NaN

var a2 = NaN;
var b2 = Number.NaN;
let c2 = NaN;
console.log(a2);
console.log(b2);
console.log(c2);

//BigInt

var a1 = 12356;
var b1 = 1234578902345678909876543;
let c1 = 345678765434567876543456789;
console.log(a1);
console.log(b1);
console.log(c1);

//Infinity

let a = Infinity;
var b = -Infinity;
const c = Infinity;
console.log(a);
console.log(b);
console.log(c);

